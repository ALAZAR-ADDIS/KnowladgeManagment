export function resolveLocale(languageLabel: string): Locale {
  return languageLabel.toLowerCase().includes("amhar") ? "am" : "en";
}

export function localeToLanguageLabel(locale: Locale): string {
  return locale === "am" ? "Amharic" : "English (ET)";
}

export function t(locale: Locale, key: string, fallback?: string): string {
  return dictionaries[locale][key] ?? fallback ?? key;
}

export function translateLoose(locale: Locale, value: string): string {
  if (locale === "en") return value;
  const exact = looseAmharicMap[value];
  if (exact) return exact;

  let transformed = value;
  for (const [from, to] of phraseReplacements) {
    if (transformed.includes(from)) {
      transformed = transformed.split(from).join(to);
    }
  }

  return transformed;
}